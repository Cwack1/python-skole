#Importer turtle som t
import turtle as t

t.speed(1)
t.shape("turtle")

#Går fremover
t.forward(100)
#Roterer 90 grader til høyre
t.right(90)
#Går bakover
t.backward(100)
#Roterer 90 grader til venstre
t.left(90)
#Venter på at vinduet skal avsluttes
t.done()

